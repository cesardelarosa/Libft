#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*r;

	r = NULL;
	while (*s)
	{
		if (*s == c)
			r = (char *)s;
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (r);
}
